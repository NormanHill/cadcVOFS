package ca.nrc.cadc.util;

import org.junit.Test;
import static org.junit.Assert.*;


public class StringUtilTest
{
    @Test
    public void matches() throws Exception
    {
        assertTrue("Should match.", StringUtil.matches("at88mph", "88m", true));
        assertTrue("Should match.", StringUtil.matches("at88mph", "aT8", true));
        assertFalse("Should not match.", StringUtil.matches("at88mph", "aT8",
                                                            false));
        assertFalse("Should match.", StringUtil.matches("at88mph",
                                                        "aT.*8[mp]?h", true));
    }
}
